type promptPlaceholder struct {
	key     string
	resolve func(emotionEmoji, skipMention bool) string
}

var promptPlaceholders = []promptPlaceholder{
	{"{{RULE5_TITLE}}", func(e, _ bool) string {
		if e {
			return activePrompts.Rule5TitleWithEmotion
		}
		return activePrompts.Rule5TitleNoEmotion
	}},
	{"{{EMOTION_SECTION}}", func(e, _ bool) string {
		if e {
			return activePrompts.EmotionSection
		}
		return ""
	}},
	{"{{EMOTION_EXAMPLES}}", func(e, _ bool) string {
		if e {
			return activePrompts.EmotionExamples
		}
		return ""
	}},
	{"{{EDIT_ONLY_EMOTION_EXAMPLES}}", func(e, _ bool) string {
		if e {
			return editOnlyEmotionExamplesSection
		}
		return ""
	}},
	{"{{EDIT_ONLY_EMOTION_SECTION}}", func(e, _ bool) string {
		if e {
			return editOnlyEmotionSection
		}
		return ""
	}},
	{"{{MENTION_SECTION}}", func(_ bool, s bool) string {
		if !s {
			return activePrompts.MentionSection
		}
		return ""
	}},
}

func BuildSystemMessage(emotionEmoji, skipMention bool, mode ...string) string {
	m := ""
	if len(mode) > 0 {
		m = mode[0]
	}

	// Select template based on mode.
	// When only `system` was overridden via YAML (backward compat), fall back
	// to the custom system prompt for modes that have no explicit override.
	var tmpl string
	switch m {
	case "append":
		if activePrompts.AppendOnlyOverridden {
			tmpl = activePrompts.SystemAppendOnly
		} else if activePrompts.SystemOverridden {
			tmpl = activePrompts.System
		} else {
			tmpl = activePrompts.SystemAppendOnly
		}
	case "edit_only":
		if activePrompts.EditOnlyOverridden {
			tmpl = activePrompts.SystemEditOnly
		} else if activePrompts.SystemOverridden {
			tmpl = activePrompts.System
		} else {
			tmpl = activePrompts.SystemEditOnly
		}
	default:
		tmpl = activePrompts.System
	}

	var pairs []string
	for _, p := range promptPlaceholders {
		if strings.Contains(tmpl, p.key) {
			pairs = append(pairs, p.key, p.resolve(emotionEmoji, skipMention))
		}
	}
	// 编辑模式条件注入(仅 smart/edit 模板需要)
	if strings.Contains(tmpl, editSectionPlaceholder) {
		if m == "edit" || m == "edit_only" {
			pairs = append(pairs, editSectionPlaceholder, editSection)
		} else {
			pairs = append(pairs, editSectionPlaceholder, "")
		}
	}
	if len(pairs) == 0 {
		return tmpl
	}
	return strings.NewReplacer(pairs...).Replace(tmpl)
}

func BuildVocabularyReference(personalCtx, memberCtx, chatCtx string) string {
	if personalCtx == "" && memberCtx == "" {
		return chatCtx
	}

	var parts []string

	if personalCtx != "" {
		parts = append(parts, "用户个人设置的纠错上下文:\n<personal_vocabulary>\n"+personalCtx+"\n</personal_vocabulary>")
	}

	if memberCtx != "" {
		parts = append(parts, "聊天成员上下文:\n<member_vocabulary>\n"+memberCtx+"\n</member_vocabulary>")
	}

	if chatCtx != "" {
		parts = append(parts, "最近的聊天消息内容:\n<latest_chat_context>\n"+chatCtx+"\n</latest_chat_context>")
	}

	return strings.Join(parts, "\n")
}

func BuildUserMessage(mode, contextText, chatContext string, emotionEmoji bool) string {
	var parts []string

	hasVocab := chatContext != ""

	if hasVocab {
		parts = append(parts, fmt.Sprintf(activePrompts.VocabularyReference, chatContext))
	}

	switch mode {
	case "append":
		if contextText != "" {
			if hasVocab {
				parts = append(parts, fmt.Sprintf(activePrompts.AppendInputBuffer, contextText))
			} else {
				parts = append(parts, fmt.Sprintf(activePrompts.AppendInputBufferNoVocab, contextText))
			}
			if emotionEmoji {
				parts = append(parts, activePrompts.TaskAppend)
			} else {
				parts = append(parts, activePrompts.TaskAppendNoEmotion)
			}
		} else {
			if hasVocab {
				parts = append(parts, activePrompts.TaskTranscribeWithVocab)
			} else {
				parts = append(parts, activePrompts.TaskTranscribe)
			}
		}
	case "edit":
		if contextText != "" {
			parts = append(parts, fmt.Sprintf(activePrompts.EditInputBuffer, contextText))
			if emotionEmoji {
				parts = append(parts, activePrompts.TaskEdit)
			} else {
				parts = append(parts, activePrompts.TaskEditNoEmotion)
			}
		} else {
			if hasVocab {
				parts = append(parts, activePrompts.TaskTranscribeWithVocab)
			} else {
				parts = append(parts, activePrompts.TaskTranscribe)
			}
		}
	case "edit_only":
		if contextText != "" {
			parts = append(parts, fmt.Sprintf(activePrompts.EditInputBuffer, contextText))
			if emotionEmoji {
				parts = append(parts, activePrompts.TaskEditOnly)
			} else {
				parts = append(parts, activePrompts.TaskEditOnlyNoEmotion)
			}
		} else {
			if hasVocab {
				parts = append(parts, activePrompts.TaskTranscribeWithVocab)
			} else {
				parts = append(parts, activePrompts.TaskTranscribe)
			}
		}
	default:
		if hasVocab {
			parts = append(parts, activePrompts.TaskTranscribeWithVocab)
		} else {
			parts = append(parts, activePrompts.TaskTranscribe)
		}
	}

	return strings.Join(parts, "\n\n")
}

func IsNoSpeech(text string) bool {
	if text == "" {
		return true
	}
	trimmed := strings.TrimSpace(text)
	return strings.Contains(trimmed, NoSpeechSentinel)
}
